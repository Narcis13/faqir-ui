#!/usr/bin/env node
/**
 * Build the `@faqir-ui/core` runtime package (CDN artifacts + SRI).  [task 0.3-02]
 *
 * Produces, into `packages/core/dist/`:
 *   - `faqir-core.js`        — canonical UMD engine (copied from registry/core)
 *   - `faqir-core.min.js`    — minified classic-script build (+ `.map` sourcemap)
 *   - `faqir.{theme}.css`    — one prebuilt full CSS bundle per registry theme
 *   - `plugins/`             — official plugin drops (empty for now; see §A5)
 *   - `sri.json`             — SHA-384 subresource-integrity hashes for every file
 *
 * The minified engine is bundled from `packages/core/src/cdn-entry.js` with
 * `bun build --format=iife` so a plain `<script src>` sets `window.Faqir`.
 * Requires Bun to *build* the min bundle — but the artifacts run anywhere.
 *
 * Runnable via `bun run build:core-package` or `node scripts/build-core-package.mjs`.
 */
import { spawnSync } from "node:child_process";
import { createHash } from "node:crypto";
import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { gzipSync } from "node:zlib";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const REGISTRY = join(ROOT, "registry");
const PKG = join(ROOT, "packages", "core");
const DIST = join(PKG, "dist");
const ENGINE_SRC = join(REGISTRY, "core", "faqir-core.js");
const CDN_ENTRY = join(PKG, "src", "cdn-entry.js");

// Engine gzip budget (§A6). Over-budget is a printed note today, not a failure:
// recipe controllers are still inlined in the engine (dedup lands in 0.3-04) and
// the engine/controller split lands in 0.3-03.
const ENGINE_GZIP_BUDGET = 14 * 1024;

// Token files in cascade order (mirrors src/utils/bundler.ts). We concatenate the
// individual files rather than tokens/index.css precisely because index.css is a
// list of `@import`s — the shipped bundles must be self-contained (no @import).
const TOKEN_FILES_ORDERED = [
  "palette.css",
  "spacing.css",
  "typography.css",
  "effects.css",
  "motion.css",
  "semantic.css",
  "aliases.css",
  "document.css",
  "doc-aliases.css",
];

const BASE_FILES_ORDERED = ["reset.css", "prose.css"];

function listCss(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir).filter((name) => statSync(join(dir, name)).isDirectory()).sort();
}

function section(label, filePath) {
  if (!existsSync(filePath)) return null;
  const body = readFileSync(filePath, "utf8").trimEnd();
  if (!body) return null;
  return `/* === ${label} === */\n${body}`;
}

/** Assemble one full CSS bundle for a single theme. */
function buildThemeCss(themeName) {
  const sections = [];
  const push = (label, filePath) => {
    const s = section(label, filePath);
    if (s) sections.push(s);
  };

  // 1. Tokens (individual files, in cascade order — never tokens/index.css)
  for (const file of TOKEN_FILES_ORDERED) {
    push(`tokens/${file}`, join(REGISTRY, "tokens", file));
  }
  // 2. Theme overrides
  push(`themes/${themeName}.css`, join(REGISTRY, "themes", `${themeName}.css`));
  // 3. Base
  for (const file of BASE_FILES_ORDERED) {
    push(`base/${file}`, join(REGISTRY, "base", file));
  }
  // 4/5/6. Primitives, recipes, patterns (alphabetical)
  for (const kind of ["primitives", "recipes", "patterns"]) {
    for (const name of listCss(join(REGISTRY, kind))) {
      push(`${kind}/${name}.css`, join(REGISTRY, kind, name, `${name}.css`));
    }
  }

  const header =
    `/* @faqir-ui/core — faqir.${themeName}.css */\n` +
    `/* Prebuilt full CSS bundle: all tokens + the "${themeName}" theme + base + every component. */\n` +
    `/* Generated by scripts/build-core-package.mjs. Fully inlined and self-contained. */\n\n`;
  return header + sections.join("\n\n") + "\n";
}

function minifyEngine() {
  const bun = process.env.FAQIR_BUN || "bun";
  const result = spawnSync(
    bun,
    [
      "build",
      CDN_ENTRY,
      "--minify",
      "--format=iife",
      "--sourcemap=linked",
      `--outdir=${DIST}`,
      "--entry-naming=faqir-core.min.js",
    ],
    { stdio: ["ignore", "pipe", "inherit"], cwd: ROOT, encoding: "utf8" }
  );

  if (result.error) {
    if (result.error.code === "ENOENT") {
      process.stderr.write(
        "build:core-package needs Bun to minify the engine bundle. Install Bun from https://bun.sh and retry.\n"
      );
      process.exit(1);
    }
    process.stderr.write(`build:core-package failed to launch ${bun}: ${result.error.message}\n`);
    process.exit(1);
  }
  if (result.status !== 0) process.exit(result.status ?? 1);
}

/** SHA-384 subresource-integrity token for a buffer. */
function sriHash(buf) {
  return "sha384-" + createHash("sha384").update(buf).digest("base64");
}

/** Recursively list every file under `dir`, as paths relative to `dir`. */
function walk(dir, prefix = "") {
  const out = [];
  for (const name of readdirSync(dir).sort()) {
    const abs = join(dir, name);
    const rel = prefix ? `${prefix}/${name}` : name;
    if (statSync(abs).isDirectory()) out.push(...walk(abs, rel));
    else out.push(rel);
  }
  return out;
}

// ── Build ────────────────────────────────────────────────────────────────────

if (!existsSync(ENGINE_SRC)) {
  process.stderr.write(`Engine source not found at ${relative(ROOT, ENGINE_SRC)}\n`);
  process.exit(1);
}

rmSync(DIST, { recursive: true, force: true });
mkdirSync(DIST, { recursive: true });

// 1. Canonical UMD engine (unminified) — used by import/require and as a CDN fallback.
copyFileSync(ENGINE_SRC, join(DIST, "faqir-core.js"));

// 2. Minified classic-script engine (+ sourcemap).
minifyEngine();

// 3. Per-theme CSS bundles.
const themes = readdirSync(join(REGISTRY, "themes"))
  .filter((f) => f.endsWith(".css"))
  .map((f) => f.replace(/\.css$/, ""))
  .sort();
for (const theme of themes) {
  writeFileSync(join(DIST, `faqir.${theme}.css`), buildThemeCss(theme));
}

// 4. Plugins folder. Copy any registry/core/plugins/*.js drops; otherwise leave a
//    documented placeholder so the CDN path `dist/plugins/` always exists (§A5).
const PLUGINS_SRC = join(REGISTRY, "core", "plugins");
const PLUGINS_DIST = join(DIST, "plugins");
mkdirSync(PLUGINS_DIST, { recursive: true });
const pluginFiles = existsSync(PLUGINS_SRC)
  ? readdirSync(PLUGINS_SRC).filter((f) => f.endsWith(".js")).sort()
  : [];
for (const file of pluginFiles) {
  copyFileSync(join(PLUGINS_SRC, file), join(PLUGINS_DIST, file));
}
if (pluginFiles.length === 0) {
  writeFileSync(
    join(PLUGINS_DIST, "README.md"),
    "# @faqir-ui/core plugins\n\n" +
      "Official plugins (`faqir-persist`, `faqir-intersect`, `faqir-mask`, " +
      "`faqir-collapse`, `faqir-validate` — see FAQIR-NEXT §A5) ship here as they land, " +
      "each self-registering via `Faqir.plugin` and loadable as a separate `<script>` tag. " +
      "None are published yet.\n"
  );
}

// 5. SRI over every emitted file (sri.json itself excluded — it is written last).
const sri = {};
for (const rel of walk(DIST)) {
  if (rel === "sri.json") continue;
  sri[rel] = sriHash(readFileSync(join(DIST, rel)));
}
writeFileSync(join(DIST, "sri.json"), JSON.stringify(sri, null, 2) + "\n");

// 6. Report + size budget.
const minPath = join(DIST, "faqir-core.min.js");
const minRaw = statSync(minPath).size;
const minGzip = gzipSync(readFileSync(minPath)).length;
const kb = (n) => (n / 1024).toFixed(2);

console.log(`✓ Built @faqir-ui/core → ${relative(ROOT, DIST)}`);
console.log(`  faqir-core.js        ${kb(statSync(join(DIST, "faqir-core.js")).size)} KB (UMD)`);
console.log(`  faqir-core.min.js    ${kb(minRaw)} KB raw · ${kb(minGzip)} KB gzip`);
console.log(`  themes               ${themes.map((t) => `faqir.${t}.css`).join(", ")}`);
console.log(`  plugins              ${pluginFiles.length ? pluginFiles.join(", ") : "(none yet)"}`);
console.log(`  sri.json             ${Object.keys(sri).length} files hashed (SHA-384)`);

if (minGzip <= ENGINE_GZIP_BUDGET) {
  console.log(`  size budget          OK — ${kb(minGzip)} KB ≤ ${kb(ENGINE_GZIP_BUDGET)} KB gzip`);
} else {
  console.log(
    `  size budget          NOTE — ${kb(minGzip)} KB > ${kb(ENGINE_GZIP_BUDGET)} KB gzip. ` +
      `Recipe controllers are still inlined in the engine; the engine/controller split (0.3-03) ` +
      `and controller de-duplication (0.3-04) bring this under budget.`
  );
}

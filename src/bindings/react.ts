// React emitter for the bindings codegen (task 0.7-01, FAQIR-NEXT §11.3).
// Consumes the SAME target-agnostic IRs as the Vue emitter (src/bindings/ir.ts —
// the single manifest-walking logic across every binding target) and emits one
// TypeScript module per primitive: a typed spec passed to the hand-written
// `createFaqirPrimitive` runtime, which builds a `forwardRef` function component.
//
// RSC-safe: neither the generated modules nor the runtime carry a `"use client"`
// directive or any client-only hook — primitives are pure attribute-contract
// renderers usable inside a server component. Output is deterministic (sorted
// components, manifest-order members) so regeneration is byte-stable — the drift
// guard depends on that.

import { pascalCase, type ComponentIR, type IRState } from "./ir";
import type { RecipeBundle, RecipeIR } from "./recipe-ir";

const HEADER = (ir: ComponentIR) =>
  `// AUTO-GENERATED by \`faqir bindings react\` from ${ir.manifestPath}.
// Do not edit — regenerate with \`faqir bindings react\`.
`;

function quote(s: string): string {
  return JSON.stringify(s);
}

function union(values: string[]): string {
  return values.map(quote).join(" | ");
}

/** An interface member name — quoted when it isn't a bare JS identifier (kebab
 * slot parts like `nav-prev` are valid JSX prop names but must be quoted keys). */
function memberName(name: string): string {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : quote(name);
}

function stateDoc(s: IRState): string {
  if (s.kind === "aria") return `Reflected as \`${s.attr}="true"|"false"\`.`;
  if (s.kind === "presence") return `Sets the \`${s.attr}\` attribute.`;
  return `Sets \`${s.attr}="${s.value}"\`.`;
}

/** Every Faqir-declared prop name for this primitive, in declaration order. */
function declaredProps(ir: ComponentIR): string[] {
  return [
    ...ir.variants.map((v) => v.prop),
    ...ir.states.map((s) => s.prop),
    ...ir.slots.map((s) => s.name),
  ];
}

/** Emit the TypeScript module for one primitive. */
export function emitReactComponent(ir: ComponentIR): string {
  const lines: string[] = [HEADER(ir)];
  lines.push(`import { createFaqirPrimitive } from "../runtime";`);
  // Slot content is typed as ReactNode; the base props come from the intrinsic
  // element so DOM attributes (className, onClick, …) and children pass through.
  const needsReactNode = ir.slots.length > 0;
  lines.push(
    needsReactNode
      ? `import type { ComponentPropsWithoutRef, ReactNode } from "react";`
      : `import type { ComponentPropsWithoutRef } from "react";`
  );
  lines.push("");

  // Literal union type per variant group.
  for (const v of ir.variants) {
    const typeName = `${ir.componentName}${pascalCase(v.prop)}`;
    lines.push(
      `/** Allowed \`${v.prop}\` values (manifest variant group ${quote(v.group)}, attr \`${v.attr}\`). */`
    );
    lines.push(`export type ${typeName} = ${union(v.values)};`);
    lines.push("");
  }

  // Props interface. Faqir-declared names are Omitted from the intrinsic element
  // base first — several collide with DOM attributes at an incompatible type
  // (`size` is `number` on <input>, `title` is `string` everywhere) and would
  // otherwise make the interface fail to extend.
  const declared = declaredProps(ir);
  const base = `ComponentPropsWithoutRef<${quote(ir.tag)}>`;
  const baseType =
    declared.length > 0 ? `Omit<${base}, ${union(declared)}>` : base;
  lines.push(`export interface ${ir.componentName}Props extends ${baseType} {`);
  for (const v of ir.variants) {
    const typeName = `${ir.componentName}${pascalCase(v.prop)}`;
    lines.push(`  /** \`${v.attr}\`; omitted when unset (manifest default: ${quote(v.default)}). */`);
    lines.push(`  ${v.prop}?: ${typeName};`);
  }
  for (const s of ir.states) {
    lines.push(`  /** ${stateDoc(s)} */`);
    lines.push(`  ${s.prop}?: boolean;`);
  }
  for (const sl of ir.slots) {
    lines.push(`  /** Projected into \`<${sl.tag} data-part="${sl.name}">\`${sl.required ? " (required part — always rendered)" : ""}. */`);
    lines.push(`  ${sl.name}?: ReactNode;`);
  }
  lines.push(`}`);
  lines.push("");

  // Component definition — pure spec, interpreted by the runtime forwardRef.
  lines.push(`/** \`${ir.name}\` — ${ir.description.replace(/\*\//g, "*\\/")} */`);
  lines.push(`export const ${ir.componentName} = createFaqirPrimitive<${ir.componentName}Props>({`);
  lines.push(`  name: ${quote(ir.name)},`);
  lines.push(`  tag: ${quote(ir.tag)},`);

  if (ir.variants.length === 0) {
    lines.push(`  variants: [],`);
  } else {
    lines.push(`  variants: [`);
    for (const v of ir.variants) {
      lines.push(`    { prop: ${quote(v.prop)}, attr: ${quote(v.attr)}, values: [${v.values.map(quote).join(", ")}] },`);
    }
    lines.push(`  ],`);
  }

  if (ir.states.length === 0) {
    lines.push(`  states: [],`);
  } else {
    lines.push(`  states: [`);
    for (const s of ir.states) {
      const value = s.value === null ? "null" : quote(s.value);
      lines.push(`    { prop: ${quote(s.prop)}, attr: ${quote(s.attr)}, value: ${value}, kind: ${quote(s.kind)} },`);
    }
    lines.push(`  ],`);
  }

  if (ir.slots.length === 0) {
    lines.push(`  slots: [],`);
  } else {
    lines.push(`  slots: [`);
    for (const sl of ir.slots) {
      lines.push(`    { name: ${quote(sl.name)}, tag: ${quote(sl.tag)}, required: ${sl.required}, isVoid: ${sl.isVoid} },`);
    }
    lines.push(`  ],`);
  }

  lines.push(`  defaultSlot: ${ir.defaultSlot},`);
  lines.push(`}, ${quote(ir.componentName)});`);
  lines.push("");
  lines.push(`export default ${ir.componentName};`);
  lines.push("");
  return lines.join("\n");
}

const RECIPE_HEADER = (ir: RecipeIR) =>
  `// AUTO-GENERATED by \`faqir bindings react\` from ${ir.manifestPath}
// and the recipe controller. Do not edit — regenerate with \`faqir bindings react\`.
`;

/** `page-change` → `onPageChange` — the React callback prop for a `faqir:` event. */
function eventProp(event: string): string {
  return `on${pascalCase(event)}`;
}

/**
 * Emit the TypeScript module for one recipe: the `"use client"` directive
 * (recipe wrappers are the client boundary — primitives never carry it), typed
 * props (variant unions, template string props, boolean toggles, named-slot
 * `ReactNode`s, and `on<Event>` callbacks), the vendored controller import, and
 * the spec — including the render tree parsed from the manifest reference
 * template — passed to the hand-written `createFaqirRecipe` runtime.
 */
export function emitReactRecipe(ir: RecipeIR): string {
  const lines: string[] = [RECIPE_HEADER(ir)];
  // The directive must be the first statement of the module (after comments).
  lines.push(`"use client";`);
  lines.push("");
  lines.push(`import { createFaqirRecipe } from "../recipe-runtime";`);
  lines.push(`import type { FaqirEventHandler, RecipeNode } from "../recipe-runtime";`);
  lines.push(`import type { ComponentPropsWithoutRef, ReactNode } from "react";`);
  lines.push(`import { ${ir.factoryName} } from "../controllers/${ir.controller}";`);
  lines.push("");

  for (const v of ir.variantProps) {
    const typeName = `${ir.componentName}${pascalCase(v.prop)}`;
    lines.push(
      `/** Allowed \`${v.prop}\` values (manifest variant group ${quote(v.group)}, attr \`${v.attr}\` on ${v.part}). */`
    );
    lines.push(`export type ${typeName} = ${union(v.values)};`);
    lines.push("");
  }

  // A slot and a string prop can share a name (dialog's `title` is both the
  // heading text default AND a projectable slot). React props are a flat
  // namespace, so the slot wins — `ReactNode` is a superset of `string`, so a
  // caller can still pass a plain string. The string member is dropped.
  const slotSet = new Set(ir.slots);
  const stringOnly = ir.stringProps.filter((sp) => !slotSet.has(sp.prop));
  const stringDefaults = new Map(ir.stringProps.map((sp) => [sp.prop, sp.default]));

  // Faqir-declared prop names collide with the intrinsic element's attributes
  // (`title`, `size`, the `on<Event>` DOM handlers) at incompatible types, so
  // they are Omitted from the base before the interface adds them back typed.
  const declared = [
    ...new Set([
      "id",
      ...ir.stringProps.map((sp) => sp.prop),
      ...ir.boolProps,
      ...ir.variantProps.map((v) => v.prop),
      ...ir.slots,
      ...ir.events.map(eventProp),
    ]),
  ];
  const base = `ComponentPropsWithoutRef<${quote(ir.tree.tag)}>`;
  const baseType = declared.length > 0 ? `Omit<${base}, ${union(declared)}>` : base;

  lines.push(`export interface ${ir.componentName}Props extends ${baseType} {`);
  lines.push(`  /** Root/ARIA id base; auto-generated per instance when unset. */`);
  lines.push(`  id?: string;`);
  for (const sp of stringOnly) {
    lines.push(`  /** Template text/attr substitution (default: ${quote(sp.default)}). */`);
    lines.push(`  ${sp.prop}?: string;`);
  }
  for (const bp of ir.boolProps) {
    lines.push(`  /** Toggles the template's bare data attribute. */`);
    lines.push(`  ${bp}?: boolean;`);
  }
  for (const v of ir.variantProps) {
    lines.push(`  /** \`${v.attr}\` on ${v.part}; omitted when unset (manifest default: ${quote(v.default)}). */`);
    lines.push(`  ${v.prop}?: ${ir.componentName}${pascalCase(v.prop)};`);
  }
  for (const slot of ir.slots) {
    const textFallback = stringDefaults.has(slot)
      ? ` Text fallback when unset (default: ${quote(stringDefaults.get(slot)!)}).`
      : "";
    lines.push(`  /** Content projected into the \`[data-part="${slot}"]\` element.${textFallback} */`);
    lines.push(`  ${memberName(slot)}?: ReactNode;`);
  }
  for (const ev of ir.events) {
    lines.push(`  /** \`faqir:${ev}\` → \`(detail, event)\`. */`);
    lines.push(`  ${eventProp(ev)}?: FaqirEventHandler;`);
  }
  lines.push(`}`);
  lines.push("");

  const eventDocs = ir.events.length
    ? ` Emits: ${ir.events.map((e) => `\`${eventProp(e)}\``).join(", ")} (payload: \`(detail, event)\`).`
    : "";
  const methodDocs = ir.methods.length
    ? ` Exposes via ref: ${ir.methods.map((m) => `\`${m}()\``).join(", ")}.`
    : "";
  lines.push(`/** \`${ir.name}\` — ${ir.description.replace(/\*\//g, "*\\/")}${eventDocs}${methodDocs} */`);
  lines.push(`export const ${ir.componentName} = createFaqirRecipe<${ir.componentName}Props>({`);
  lines.push(`  name: ${quote(ir.name)},`);
  lines.push(`  create: ${ir.factoryName},`);
  lines.push(`  methods: [${ir.methods.map(quote).join(", ")}],`);
  lines.push(`  events: [${ir.events.map(quote).join(", ")}],`);
  lines.push(`  slots: [${ir.slots.map(quote).join(", ")}],`);
  lines.push(`  stringProps: [${ir.stringProps.map((sp) => `{ prop: ${quote(sp.prop)}, default: ${quote(sp.default)} }`).join(", ")}],`);
  lines.push(`  boolProps: [${ir.boolProps.map(quote).join(", ")}],`);
  lines.push(`  variantProps: [`);
  for (const v of ir.variantProps) {
    lines.push(`    { prop: ${quote(v.prop)}, values: [${v.values.map(quote).join(", ")}] },`);
  }
  lines.push(`  ],`);
  // The tree is emitted as JSON; the tuple-typed RecipeNode needs the assertion
  // because JSON literals infer plain arrays, not tuples.
  lines.push(`  tree: ${JSON.stringify(ir.tree)} as unknown as RecipeNode,`);
  lines.push(`}, ${quote(ir.componentName)});`);
  lines.push("");
  lines.push(`export default ${ir.componentName};`);
  lines.push("");
  return lines.join("\n");
}

const VENDOR_NOTE = (from: string) =>
  `// AUTO-GENERATED by \`faqir bindings react\` — vendored verbatim from
// ${from} (imports rewritten to package-local paths).
// Do not edit — regenerate with \`faqir bindings react\`. The registry file is
// the single source of truth; the drift guard keeps this copy in sync.
// @ts-nocheck — registry controllers are plain JS by design (zero build step).
`;

/** Vendor one recipe controller: rewrite imports to package-local modules. */
export function emitReactController(name: string, source: string): string {
  const rewritten = source
    .replace(/from\s+"\.\.\/\.\.\/core\/([\w-]+)\.js"/g, 'from "./_core-$1"')
    .replace(/from\s+"\.\.\/([\w-]+)\/[\w-]+\.js"/g, 'from "./$1"');
  return VENDOR_NOTE(`registry/recipes/${name}/${name}.js`) + rewritten;
}

/** Vendor one registry/core helper module (focus, events, utils). */
export function emitReactCoreHelper(name: string, source: string): string {
  return VENDOR_NOTE(`registry/core/${name}.js`) + source;
}

/** Emit the package barrel re-exporting every component, recipe, and its types. */
export function emitReactIndex(irs: ComponentIR[], recipeIRs: RecipeIR[]): string {
  const lines: string[] = [
    `// AUTO-GENERATED by \`faqir bindings react\`. Do not edit — regenerate instead.`,
    ``,
    `export { createFaqirPrimitive } from "./runtime";`,
    `export type { FaqirPrimitiveSpec, SpecVariant, SpecState, SpecSlot } from "./runtime";`,
    `export { createFaqirRecipe, useFaqirController, __activeControllers } from "./recipe-runtime";`,
    `export type {`,
    `  FaqirRecipeSpec,`,
    `  RecipeNode,`,
    `  RecipeSeg,`,
    `  RecipeAttrVal,`,
    `  RecipeController,`,
    `  RecipeHandle,`,
    `  FaqirEventHandler,`,
    `  UseFaqirControllerOptions,`,
    `} from "./recipe-runtime";`,
    ``,
  ];
  for (const ir of irs) {
    const types = [
      `${ir.componentName}Props`,
      ...ir.variants.map((v) => `${ir.componentName}${pascalCase(v.prop)}`),
    ];
    lines.push(`export { ${ir.componentName} } from "./components/${ir.name}";`);
    lines.push(`export type { ${types.join(", ")} } from "./components/${ir.name}";`);
  }
  lines.push("");
  for (const ir of recipeIRs) {
    const types = [
      `${ir.componentName}Props`,
      ...ir.variantProps.map((v) => `${ir.componentName}${pascalCase(v.prop)}`),
    ];
    lines.push(`export { ${ir.componentName} } from "./recipes/${ir.name}";`);
    lines.push(`export type { ${types.join(", ")} } from "./recipes/${ir.name}";`);
  }
  lines.push("");
  // Controller factories, for the low-level `useFaqirController` escape hatch.
  for (const ir of recipeIRs) {
    lines.push(`export { ${ir.factoryName} } from "./controllers/${ir.controller}";`);
  }
  lines.push("");
  return lines.join("\n");
}

/** Full generation manifest: relative path → file content. */
export function emitReactPackage(irs: ComponentIR[], recipes: RecipeBundle): Map<string, string> {
  const files = new Map<string, string>();
  for (const ir of irs) {
    files.set(`components/${ir.name}.ts`, emitReactComponent(ir));
  }
  for (const ir of recipes.irs) {
    files.set(`recipes/${ir.name}.ts`, emitReactRecipe(ir));
  }
  for (const [name, source] of [...recipes.controllers].sort(([a], [b]) => a.localeCompare(b))) {
    files.set(`controllers/${name}.ts`, emitReactController(name, source));
  }
  for (const [name, source] of [...recipes.helpers].sort(([a], [b]) => a.localeCompare(b))) {
    files.set(`controllers/_core-${name}.ts`, emitReactCoreHelper(name, source));
  }
  files.set(`index.ts`, emitReactIndex(irs, recipes.irs));
  return files;
}

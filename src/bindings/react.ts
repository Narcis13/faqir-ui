// React emitter for the bindings codegen (task 0.7-01, FAQIR-NEXT §11.3).
// Consumes the SAME target-agnostic IRs as the Vue emitter (src/bindings/ir.ts —
// the single manifest-walking logic across every binding target) and emits one
// TypeScript module per primitive: a typed spec passed to the hand-written
// `createFaqirPrimitive` runtime, which builds a `forwardRef` function component.
//
// RSC-safe: neither the generated modules nor the runtime carry a `"use client"`
// directive or any client-only hook — primitives are pure attribute-contract
// renderers usable inside a server component. Output is deterministic (sorted
// components, manifest-order members) so regeneration is byte-stable — the drift
// guard depends on that.

import { pascalCase, type ComponentIR, type IRState } from "./ir";

const HEADER = (ir: ComponentIR) =>
  `// AUTO-GENERATED by \`faqir bindings react\` from ${ir.manifestPath}.
// Do not edit — regenerate with \`faqir bindings react\`.
`;

function quote(s: string): string {
  return JSON.stringify(s);
}

function union(values: string[]): string {
  return values.map(quote).join(" | ");
}

function stateDoc(s: IRState): string {
  if (s.kind === "aria") return `Reflected as \`${s.attr}="true"|"false"\`.`;
  if (s.kind === "presence") return `Sets the \`${s.attr}\` attribute.`;
  return `Sets \`${s.attr}="${s.value}"\`.`;
}

/** Every Faqir-declared prop name for this primitive, in declaration order. */
function declaredProps(ir: ComponentIR): string[] {
  return [
    ...ir.variants.map((v) => v.prop),
    ...ir.states.map((s) => s.prop),
    ...ir.slots.map((s) => s.name),
  ];
}

/** Emit the TypeScript module for one primitive. */
export function emitReactComponent(ir: ComponentIR): string {
  const lines: string[] = [HEADER(ir)];
  lines.push(`import { createFaqirPrimitive } from "../runtime";`);
  // Slot content is typed as ReactNode; the base props come from the intrinsic
  // element so DOM attributes (className, onClick, …) and children pass through.
  const needsReactNode = ir.slots.length > 0;
  lines.push(
    needsReactNode
      ? `import type { ComponentPropsWithoutRef, ReactNode } from "react";`
      : `import type { ComponentPropsWithoutRef } from "react";`
  );
  lines.push("");

  // Literal union type per variant group.
  for (const v of ir.variants) {
    const typeName = `${ir.componentName}${pascalCase(v.prop)}`;
    lines.push(
      `/** Allowed \`${v.prop}\` values (manifest variant group ${quote(v.group)}, attr \`${v.attr}\`). */`
    );
    lines.push(`export type ${typeName} = ${union(v.values)};`);
    lines.push("");
  }

  // Props interface. Faqir-declared names are Omitted from the intrinsic element
  // base first — several collide with DOM attributes at an incompatible type
  // (`size` is `number` on <input>, `title` is `string` everywhere) and would
  // otherwise make the interface fail to extend.
  const declared = declaredProps(ir);
  const base = `ComponentPropsWithoutRef<${quote(ir.tag)}>`;
  const baseType =
    declared.length > 0 ? `Omit<${base}, ${union(declared)}>` : base;
  lines.push(`export interface ${ir.componentName}Props extends ${baseType} {`);
  for (const v of ir.variants) {
    const typeName = `${ir.componentName}${pascalCase(v.prop)}`;
    lines.push(`  /** \`${v.attr}\`; omitted when unset (manifest default: ${quote(v.default)}). */`);
    lines.push(`  ${v.prop}?: ${typeName};`);
  }
  for (const s of ir.states) {
    lines.push(`  /** ${stateDoc(s)} */`);
    lines.push(`  ${s.prop}?: boolean;`);
  }
  for (const sl of ir.slots) {
    lines.push(`  /** Projected into \`<${sl.tag} data-part="${sl.name}">\`${sl.required ? " (required part — always rendered)" : ""}. */`);
    lines.push(`  ${sl.name}?: ReactNode;`);
  }
  lines.push(`}`);
  lines.push("");

  // Component definition — pure spec, interpreted by the runtime forwardRef.
  lines.push(`/** \`${ir.name}\` — ${ir.description.replace(/\*\//g, "*\\/")} */`);
  lines.push(`export const ${ir.componentName} = createFaqirPrimitive<${ir.componentName}Props>({`);
  lines.push(`  name: ${quote(ir.name)},`);
  lines.push(`  tag: ${quote(ir.tag)},`);

  if (ir.variants.length === 0) {
    lines.push(`  variants: [],`);
  } else {
    lines.push(`  variants: [`);
    for (const v of ir.variants) {
      lines.push(`    { prop: ${quote(v.prop)}, attr: ${quote(v.attr)}, values: [${v.values.map(quote).join(", ")}] },`);
    }
    lines.push(`  ],`);
  }

  if (ir.states.length === 0) {
    lines.push(`  states: [],`);
  } else {
    lines.push(`  states: [`);
    for (const s of ir.states) {
      const value = s.value === null ? "null" : quote(s.value);
      lines.push(`    { prop: ${quote(s.prop)}, attr: ${quote(s.attr)}, value: ${value}, kind: ${quote(s.kind)} },`);
    }
    lines.push(`  ],`);
  }

  if (ir.slots.length === 0) {
    lines.push(`  slots: [],`);
  } else {
    lines.push(`  slots: [`);
    for (const sl of ir.slots) {
      lines.push(`    { name: ${quote(sl.name)}, tag: ${quote(sl.tag)}, required: ${sl.required}, isVoid: ${sl.isVoid} },`);
    }
    lines.push(`  ],`);
  }

  lines.push(`  defaultSlot: ${ir.defaultSlot},`);
  lines.push(`}, ${quote(ir.componentName)});`);
  lines.push("");
  lines.push(`export default ${ir.componentName};`);
  lines.push("");
  return lines.join("\n");
}

/** Emit the package barrel re-exporting every component and its types. */
export function emitReactIndex(irs: ComponentIR[]): string {
  const lines: string[] = [
    `// AUTO-GENERATED by \`faqir bindings react\`. Do not edit — regenerate instead.`,
    ``,
    `export { createFaqirPrimitive } from "./runtime";`,
    `export type { FaqirPrimitiveSpec, SpecVariant, SpecState, SpecSlot } from "./runtime";`,
    ``,
  ];
  for (const ir of irs) {
    const types = [
      `${ir.componentName}Props`,
      ...ir.variants.map((v) => `${ir.componentName}${pascalCase(v.prop)}`),
    ];
    lines.push(`export { ${ir.componentName} } from "./components/${ir.name}";`);
    lines.push(`export type { ${types.join(", ")} } from "./components/${ir.name}";`);
  }
  lines.push("");
  return lines.join("\n");
}

/** Full generation manifest: relative path → file content (primitives only;
 * recipes land in 0.7-02). */
export function emitReactPackage(irs: ComponentIR[]): Map<string, string> {
  const files = new Map<string, string>();
  for (const ir of irs) {
    files.set(`components/${ir.name}.ts`, emitReactComponent(ir));
  }
  files.set(`index.ts`, emitReactIndex(irs));
  return files;
}

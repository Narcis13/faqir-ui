import type { LoomConfig } from "./config";
import { copyTextFile, fileExists, writeTextFile } from "./fs";
import { resolvePackagePath, resolveRegistryPath } from "./paths";

export const CORE_FILES = [
  "dom.js",
  "events.js",
  "focus.js",
  "motion.js",
  "store.js",
  "utils.js",
] as const;

export function requiresCoreModules(config: LoomConfig): boolean {
  return config.include_core || config.installed.recipes.length > 0;
}

export async function ensureCoreModules(projectRoot: string, config: LoomConfig): Promise<void> {
  const outputRoot = resolvePackagePath(projectRoot, config.output_dir);

  for (const fileName of CORE_FILES) {
    const targetPath = resolvePackagePath(outputRoot, "core", fileName);

    if (await fileExists(targetPath)) {
      continue;
    }

    await copyTextFile(resolveRegistryPath("core", fileName), targetPath);
  }
}

export async function writeLoomScript(projectRoot: string, config: LoomConfig): Promise<void> {
  const outputRoot = resolvePackagePath(projectRoot, config.output_dir);
  await writeTextFile(resolvePackagePath(outputRoot, "loom.js"), buildLoomScript(config.installed.recipes));
}

export function buildLoomScript(recipeNames: string[]): string {
  const imports = recipeNames.map(
    (name) => `import { ${formatControllerExport(name)} } from "./recipes/${name}/${name}.js";`,
  );
  const controllerEntries = recipeNames.map((name) => `  "${name}": ${formatControllerExport(name)},`);

  return [
    "/* Generated by loom. */",
    ...imports,
    ...(imports.length > 0 ? [""] : []),
    "const controllers = {",
    ...controllerEntries,
    "};",
    "",
    "function initNode(node) {",
    "  if (!node) {",
    "    return;",
    "  }",
    "",
    '  const name = node.getAttribute?.("data-ui");',
    "",
    "  if (name && controllers[name]) {",
    "    controllers[name](node);",
    "  }",
    "",
    "  if (!node.querySelectorAll) {",
    "    return;",
    "  }",
    "",
    "  for (const [controllerName, factory] of Object.entries(controllers)) {",
    '    node.querySelectorAll(`[data-ui="${controllerName}"]`).forEach((element) => factory(element));',
    "  }",
    "}",
    "",
    "export function init(root = typeof document === \"undefined\" ? null : document) {",
    "  if (!root) {",
    "    return;",
    "  }",
    "",
    "  if (typeof document !== \"undefined\" && root !== document) {",
    "    initNode(root);",
    "  }",
    "",
    "  if (!root.querySelectorAll) {",
    "    return;",
    "  }",
    "",
    "  for (const [name, factory] of Object.entries(controllers)) {",
    '    root.querySelectorAll(`[data-ui="${name}"]`).forEach((element) => factory(element));',
    "  }",
    "}",
    "",
    "function observe() {",
    "  if (typeof document === \"undefined\" || typeof MutationObserver !== \"function\" || !document.body) {",
    "    return null;",
    "  }",
    "",
    "  const observer = new MutationObserver((mutations) => {",
    "    for (const mutation of mutations) {",
    "      for (const node of mutation.addedNodes) {",
    "        if (node?.nodeType !== 1) {",
    "          continue;",
    "        }",
    "",
    "        initNode(node);",
    "      }",
    "    }",
    "  });",
    "",
    "  observer.observe(document.body, { childList: true, subtree: true });",
    "  return observer;",
    "}",
    "",
    "let observer = null;",
    "",
    "if (typeof document !== \"undefined\") {",
    "  const start = () => {",
    "    init(document);",
    "    observer ||= observe();",
    "  };",
    "",
    "  if (document.readyState === \"loading\") {",
    "    document.addEventListener(\"DOMContentLoaded\", start, { once: true });",
    "  } else {",
    "    start();",
    "  }",
    "}",
    "",
    "export { controllers };",
    "",
  ].join("\n");
}

function formatControllerExport(name: string): string {
  return `create${name
    .split("-")
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("")}`;
}

/** Component inventory grouped by layer, derived from the loaded manifests. */
function renderInventory(byLayer: Record<Layer, Manifest[]>): string[] {
  const lines: string[] = ["## Component Inventory", ""];
  const label: Record<Layer, string> = {
    primitives: "Primitives (CSS-only)",
    recipes: "Recipes (CSS + JS controller)",
    patterns: "Patterns (composition, no JS)",
  };
  for (const layer of LAYERS) {
    const names = byLayer[layer].map((m) => m.name).sort();
    if (names.length === 0) continue;
    lines.push(`**${names.length} ${label[layer]}:** ${names.join(", ")}`);
    lines.push("");
  }
  return lines;
}


/** Load every manifest in the registry, grouped by layer, sorted by name. */
async function loadRegistryManifests(): Promise<Record<Layer, Manifest[]>> {
  const registry = getRegistryPath();
  const out: Record<Layer, Manifest[]> = { primitives: [], recipes: [], patterns: [] };
  for (const layer of LAYERS) {
    const dir = join(registry, layer);
    if (!existsSync(dir)) continue;
    const glob = new Bun.Glob("*/*.manifest.json");
    const paths: string[] = [];
    for (const rel of glob.scanSync({ cwd: dir })) paths.push(join(dir, rel));
    for (const p of paths.sort()) out[layer].push(await loadManifest(p));
  }
  return out;
}

/** Load a project's installed manifests, grouped by layer. */
async function loadInstalledManifests(cwd: string): Promise<Record<Layer, Manifest[]>> {
  const config = await readConfig(cwd);
  const out: Record<Layer, Manifest[]> = { primitives: [], recipes: [], patterns: [] };
  for (const layer of LAYERS) {
    for (const name of config.installed[layer]) {
      const path = join(cwd, config.output_dir, layer, name, `${name}.manifest.json`);
      if (existsSync(path)) out[layer].push(await loadManifest(path));
    }
  }
  return out;
}

function renderPlugins(plugins: PluginMetadata[], corePrefix: string): string[] {
  if (plugins.length === 0) return [];
  const lines: string[] = [];
  lines.push("## Official Plugins");
  lines.push("");
  lines.push("Load a plugin after `faqir-core.js`, or combine core plus every plugin with `faqir bundle --js`:");
  lines.push("");
  for (const plugin of plugins) {
    const summary = plugin.description ? ` — ${plugin.description}` : "";
    lines.push(
      `- \`${plugin.name}\` (${plugin.provides.map((item) => `\`${item}\``).join(", ")}): ` +
        `\`${corePrefix}/plugins/${plugin.file}\`${summary}`,
    );
  }
  lines.push("");
  return lines;
}


/**
 * The skill's own metadata (frontmatter). This is authored identity, not
 * manifest-derived — Claude Code matches on it to route Faqir tasks here.
 */
const SHIPPED_FRONTMATTER_DESCRIPTION =
  "Expert agent for the Faqir UI framework — generates, audits, repairs, and explains zero-class, manifest-driven UI components and pages. Use when building HTML pages or components with Faqir UI, when the user asks to create/modify/audit Faqir UI markup, when working with data-ui/data-part/data-variant/data-state attributes, when generating page layouts using Faqir tokens and primitives, when creating new registry components (primitives, recipes, patterns), when connecting pages to server data via l-source directive or apiSource(), or when building printable documents (invoices, reports, forms). Triggers on any Faqir UI task including component creation, page scaffolding, code auditing, token usage, reactive directive usage (l-data, l-model, l-for, l-source), data-driven rendering, document/print layout, and CLI operations.";

/** A single generated file destined for the shipped skill directory. */
export interface GeneratedFile {
  /** Path relative to `.claude/skills/faqir-creator/`. */
  relPath: string;
  content: string;
}

const REFERENCE_LABEL: Record<Layer, string> = {
  primitives: "Primitives",
  recipes: "Recipes",
  patterns: "Patterns",
};

/** Build one per-layer reference file (references/<layer>.md). */
function renderReferenceFile(layer: Layer, manifests: Manifest[], schemaVersion: string): string {
  const lines: string[] = [];
  lines.push(generationHeader(schemaVersion));
  lines.push("");
  lines.push(`# Faqir ${REFERENCE_LABEL[layer]} Reference`);
  lines.push("");
  lines.push(
    `${manifests.length} ${layer}, each with its anatomy tree, variant table, and safe/unsafe transforms — all derived from the component manifest.`,
  );
  lines.push("");
  for (const m of manifests) lines.push(...renderComponentSection(m, 2));
  return lines.join("\n").replace(/\n{3,}/g, "\n\n").replace(/\s+$/g, "") + "\n";
}

/** Build the shipped SKILL.md (frontmatter + framework sections + inventory + compositions). */
function renderShippedSkill(
  byLayer: Record<Layer, Manifest[]>,
  schemaVersion: string,
  plugins: PluginMetadata[],
): string {
  const lines: string[] = [];
  lines.push("---");
  lines.push("name: faqir-creator");
  lines.push(`description: ${SHIPPED_FRONTMATTER_DESCRIPTION}`);
  lines.push("---");
  lines.push("");
  lines.push(generationHeader(schemaVersion));
  lines.push("");
  lines.push("# Faqir Creator");
  lines.push("");
  lines.push(
    "Expert agent for the Faqir UI framework. Generate class-free HTML using data attributes, design tokens, and manifest contracts. Every component contract below is generated from its manifest.",
  );
  lines.push("");

  lines.push(...renderAttributeProtocol());
  lines.push(...renderStrictRules());
  lines.push(...renderInventory(byLayer));
  lines.push(...renderPlugins(plugins, "registry/core"));
  lines.push(...renderCompositions(byLayer.patterns));
  lines.push(...renderDataDriven());
  lines.push(...renderCliReference());

  lines.push("## Per-Component Reference");
  lines.push("");
  lines.push("Full anatomy trees, variant tables, and safe/unsafe transforms for every component:");
  lines.push("");
  for (const layer of LAYERS) {
    if (byLayer[layer].length === 0) continue;
    lines.push(
      `- [references/${layer}.md](references/${layer}.md) — ${byLayer[layer].length} ${REFERENCE_LABEL[layer].toLowerCase()}`,
    );
  }
  lines.push("- [references/tokens.md](references/tokens.md) — design token reference");
  lines.push("- [references/directives.md](references/directives.md) — faqir-core reactive directives");
  lines.push("- [references/manifest.md](references/manifest.md) — manifest schema and examples");
  lines.push("");

  return lines.join("\n").replace(/\n{3,}/g, "\n\n").replace(/\s+$/g, "") + "\n";
}

/**
 * Generate every file of the shipped `faqir-creator` skill from the registry:
 * the SKILL.md plus one reference file per layer. Deterministic and idempotent.
 */
export async function generateShippedSkillFiles(): Promise<GeneratedFile[]> {
  const byLayer = await loadRegistryManifests();
  const schemaVersion = await getSchemaVersion();
  const plugins = loadPluginMetadata(join(getRegistryPath(), "core", "plugins"));

  const files: GeneratedFile[] = [
    { relPath: "SKILL.md", content: renderShippedSkill(byLayer, schemaVersion, plugins) },
  ];
  for (const layer of LAYERS) {
    files.push({
      relPath: join("references", `${layer}.md`),
      content: renderReferenceFile(layer, byLayer[layer], schemaVersion),
    });
  }
  return files;
}

/** Absolute path to the shipped skill directory. */
export function shippedSkillDir(): string {
  return join(getPackageRoot(), ".claude", "skills", "faqir-creator");
}

/**
 * Write the shipped skill to `.claude/skills/faqir-creator/`. Returns the
 * absolute paths written. Used by `scripts/gen-skill.mjs`.
 */
export async function writeShippedSkill(): Promise<string[]> {
  const files = await generateShippedSkillFiles();
  const dir = shippedSkillDir();
  const written: string[] = [];
  for (const f of files) {
    const abs = join(dir, f.relPath);
    ensureDir(join(abs, ".."));
    await Bun.write(abs, f.content);
    written.push(abs);
  }
  return written;
}

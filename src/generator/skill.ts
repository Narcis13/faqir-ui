/**
 * Generate a project's `.faqir/SKILL.md`, scoped to its installed component set.
 * Self-contained (no external reference files): each installed component gets a
 * full manifest-derived section inline. Carries the generation header.
 */
export async function generateSkill(cwd: string): Promise<string> {
  const byLayer = await loadInstalledManifests(cwd);
  const schemaVersion = await getSchemaVersion();
  const config = await readConfig(cwd);
  const corePrefix = `${config.output_dir.replace(/\/$/, "")}/core`;
  const plugins = loadPluginMetadata(join(cwd, corePrefix, "plugins"));

  const lines: string[] = [];
  lines.push(generationHeader(schemaVersion));
  lines.push("");
  lines.push("# Faqir UI Framework Skill");
  lines.push("");
  lines.push("Zero-class, manifest-driven UI. When building UI, read `.faqir/context.json` first; this skill documents the components this project installs.");
  lines.push("");

  lines.push(...renderAttributeProtocol());
  lines.push(...renderStrictRules());
  lines.push(...renderInventory(byLayer));
  lines.push(...renderPlugins(plugins, corePrefix));

  // Aliases — alternate names installed components answer to, so an agent
  // searching e.g. "alert" discovers it maps to "callout".
  const aliasPairs: string[] = [];
  for (const layer of LAYERS) {
    for (const m of byLayer[layer]) {
      for (const alias of m.aliases ?? []) aliasPairs.push(`- \`${alias}\` → \`${m.name}\``);
    }
  }
  if (aliasPairs.length > 0) {
    lines.push("## Aliases");
    lines.push("");
    lines.push("These names resolve to the component on the right — use `faqir add <alias>` or the canonical `data-ui` value:");
    lines.push("");
    lines.push(...aliasPairs);
    lines.push("");
  }

  // Recipe controllers (interactive components need their JS controller loaded).
  if (byLayer.recipes.length > 0) {
    lines.push("## Recipe Controllers");
    lines.push("");
    lines.push("Interactive components auto-initialize via the core script, or import a controller directly:");
    lines.push("");
    for (const m of byLayer.recipes) {
      lines.push(
        `- \`import { ${factoryName(m.name)} } from "./${config.output_dir}/recipes/${m.name}/${m.name}.js"\``,
      );
    }
    lines.push("");
    lines.push(`Auto-init: \`<script type="module" src="${config.output_dir}/core/faqir.js"></script>\``);
    lines.push("");
  }

  // Per-component sections.
  const sectionTitle: Record<Layer, string> = {
    primitives: "Primitives",
    recipes: "Recipes",
    patterns: "Patterns",
  };
  for (const layer of LAYERS) {
    if (byLayer[layer].length === 0) continue;
    lines.push(`## ${sectionTitle[layer]}`);
    lines.push("");
    for (const m of byLayer[layer]) lines.push(...renderComponentSection(m, 3));
  }

  // Canonical compositions from installed patterns.
  const compositions = renderCompositions(byLayer.patterns);
  if (compositions.length) lines.push(...compositions);

  lines.push(...renderDataDriven());
  lines.push(...renderCliReference());

  return lines.join("\n").replace(/\n{3,}/g, "\n\n").replace(/\s+$/g, "") + "\n";
}

/** Write the project's `.faqir/SKILL.md`. */
export async function writeSkillFile(cwd: string): Promise<string> {
  const content = await generateSkill(cwd);
  const faqirDir = join(cwd, ".faqir");
  ensureDir(faqirDir);
  const outPath = join(faqirDir, "SKILL.md");
  await Bun.write(outPath, content);
  return outPath;
}


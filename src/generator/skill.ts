// Manifest-derived Claude Code skill generator (task 0.5-07, FAQIR-PLAN §8.2).
//
// Replaces the old static SKILL.md template. Every per-component section —
// anatomy tree, variant table, safe/unsafe transforms — plus the canonical
// compositions are DERIVED from manifests, so the shipped `faqir-creator` skill
// is regenerated from this same pipeline (dogfood) and never hand-maintained.
//
// Two entry points share one set of renderers:
//   • generateSkill(cwd)          → a project's self-contained .faqir/SKILL.md,
//                                    scoped to its installed component set.
//   • generateShippedSkillFiles() → the registry-wide .claude/skills/faqir-creator
//                                    skill (SKILL.md + per-layer references),
//                                    covering every component in registry/.
//
// Output carries a grep-able generation header and no timestamps, so
// regeneration is byte-idempotent (gated by `bun run check:skill`).

import { existsSync } from "node:fs";
import { join } from "node:path";
import { readConfig } from "../utils/config";
import { ensureDir, getPackageRoot, getRegistryPath } from "../utils/fs";
import { loadManifest, type Manifest } from "../manifest";
import { getSchemaVersion } from "../utils/schema";
import { loadPluginMetadata, type PluginMetadata } from "./plugins";

/** Stable, grep-able marker proving a file came out of this generator. */
export const SKILL_GENERATION_MARKER = "GENERATED by faqir · manifest-derived skill";

const LAYERS = ["primitives", "recipes", "patterns"] as const;
type Layer = (typeof LAYERS)[number];

/**
 * The generation header stamped into every generated file. No timestamp — the
 * only variable is `schema_version`, so identical inputs produce identical bytes.
 */
function generationHeader(schemaVersion: string): string {
  return (
    `<!-- ${SKILL_GENERATION_MARKER} · schema_version ${schemaVersion} · ` +
    `regenerate with \`bun run gen:skill\` · do not edit by hand -->`
  );
}

/** Turn `date-picker` into `createDatePicker` (recipe controller factory). */
function factoryName(recipe: string): string {
  return "create" + recipe.split("-").map((w) => w[0].toUpperCase() + w.slice(1)).join("");
}


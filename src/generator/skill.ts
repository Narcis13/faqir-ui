/**
 * Render a component's anatomy tree: the root element followed by its named
 * slots, each annotated with its tag hint, required-ness, and description.
 * Derived entirely from `anatomy` + `slots`.
 */
function renderAnatomyTree(m: Manifest): string[] {
  const lines: string[] = ["```text"];
  const content = m.anatomy.content_model ? ` · content: ${m.anatomy.content_model}` : "";
  lines.push(`${m.anatomy.selector}  ·  <${m.anatomy.tag}>${content}`);

  const slots = Object.entries(m.slots ?? {});
  slots.forEach(([name, slot], i) => {
    const branch = i === slots.length - 1 ? "└─" : "├─";
    const tag = slot.tag_hint ? `<${slot.tag_hint}>` : "";
    const req = slot.required ? "required" : "optional";
    const desc = slot.description ? `  — ${slot.description}` : "";
    // name is informational; the real contract is the selector.
    lines.push(`${branch} ${slot.selector}  ${tag}  ${req}${desc}`.replace(/\s+$/g, ""));
  });

  lines.push("```");
  return lines;
}

/** Render a markdown variant table from `variants` (empty → a single note). */
function renderVariantTable(m: Manifest): string[] {
  const entries = Object.entries(m.variants ?? {});
  if (entries.length === 0) return ["_No variants._"];

  const lines: string[] = [
    "| Variant | Values | Default | Attribute | Applied to |",
    "|---------|--------|---------|-----------|------------|",
  ];
  for (const [name, v] of entries) {
    const values = v.values.map((x) => `\`${x}\``).join(", ");
    lines.push(
      `| ${name} | ${values} | \`${v.default}\` | \`${v.attr}\` | ${v.applied_to ?? "root"} |`,
    );
  }
  return lines;
}

/** Render the safe / unsafe transform lists. */
function renderTransforms(m: Manifest): string[] {
  const lines: string[] = [];
  if (m.safe_transforms?.length) {
    lines.push(`- **Safe transforms:** ${m.safe_transforms.map((t) => `\`${t}\``).join(", ")}`);
  }
  if (m.unsafe_transforms?.length) {
    lines.push(`- **Unsafe (never do):** ${m.unsafe_transforms.map((t) => `\`${t}\``).join(", ")}`);
  }
  return lines;
}

/** One-line accessibility summary derived from `a11y`. */
function renderA11y(m: Manifest): string[] {
  const a = m.a11y;
  if (!a) return [];
  const parts: string[] = [];
  if (a.role) parts.push(`role=${a.role}`);
  if (a["aria-modal"]) parts.push("aria-modal");
  if (a.focus_trap) parts.push("focus-trap");
  if (a.escape_closes) parts.push("escape-closes");
  if (a.return_focus) parts.push(`return-focus→${a.return_focus}`);
  const keys = a.keyboard ? Object.keys(a.keyboard) : [];
  if (keys.length) parts.push(`keys: ${keys.join(", ")}`);
  if (parts.length === 0 && (!a.required_attrs || a.required_attrs.length === 0)) return [];
  const lines: string[] = [];
  if (parts.length) lines.push(`- **A11y:** ${parts.join(" · ")}`);
  if (a.required_attrs?.length) {
    lines.push(`- **Required ARIA:** ${a.required_attrs.map((x) => `\`${x}\``).join("; ")}`);
  }
  return lines;
}

/** The canonical HTML template for a component (first of `html`, else first key). */
function primaryTemplate(m: Manifest): string | undefined {
  const t = m.templates ?? {};
  if (typeof t.html === "string") return t.html;
  const first = Object.values(t)[0];
  return typeof first === "string" ? first : undefined;
}

/**
 * A full per-component reference section: heading, meta line, description,
 * canonical template, anatomy tree, variant table, transforms, and a11y.
 * `headingLevel` lets the caller nest it (### in references, #### inline).
 */
function renderComponentSection(m: Manifest, headingLevel = 3): string[] {
  const h = "#".repeat(headingLevel);
  const lines: string[] = [];
  lines.push(`${h} ${m.name}`);
  lines.push("");
  const meta = [`kind: ${m.kind}`];
  if (m.category) meta.push(`category: ${m.category}`);
  if (m.aliases?.length) meta.push(`aliases: ${m.aliases.join(", ")}`);
  if (m.files?.js) meta.push(`controller: ${factoryName(m.name)}()`);
  lines.push(`_${meta.join(" · ")}_`);
  lines.push("");
  if (m.description) {
    lines.push(m.description);
    lines.push("");
  }

  const tpl = primaryTemplate(m);
  if (tpl) {
    lines.push("```html");
    lines.push(tpl);
    lines.push("```");
    lines.push("");
  }

  lines.push("**Anatomy**");
  lines.push("");
  lines.push(...renderAnatomyTree(m));
  lines.push("");

  lines.push("**Variants**");
  lines.push("");
  lines.push(...renderVariantTable(m));
  lines.push("");

  const transforms = renderTransforms(m);
  const a11y = renderA11y(m);
  if (transforms.length || a11y.length) {
    lines.push(...transforms);
    lines.push(...a11y);
    lines.push("");
  }

  return lines;
}

/**
 * Render up to `limit` canonical compositions from pattern manifests. A pattern
 * IS a canonical composition — its template wires primitives/recipes together
 * and `composition.contains` names them. Deterministic: patterns sorted by name.
 */
function renderCompositions(patterns: Manifest[], limit = 3): string[] {
  const chosen = [...patterns].sort((a, b) => a.name.localeCompare(b.name)).slice(0, limit);
  if (chosen.length === 0) return [];

  const lines: string[] = ["## Canonical Compositions", ""];
  lines.push(
    "Real component compositions, taken verbatim from pattern manifests. Each shows how primitives and recipes assemble into a working unit.",
  );
  lines.push("");
  for (const p of chosen) {
    lines.push(`### ${p.name}`);
    lines.push("");
    if (p.description) {
      lines.push(p.description);
      lines.push("");
    }
    const uses = p.composition?.contains ?? [];
    if (uses.length) {
      lines.push(`Composes: ${uses.map((u) => `\`${u}\``).join(", ")}`);
      lines.push("");
    }
    const tpl = primaryTemplate(p);
    if (tpl) {
      lines.push("```html");
      lines.push(tpl);
      lines.push("```");
      lines.push("");
    }
  }
  return lines;
}

